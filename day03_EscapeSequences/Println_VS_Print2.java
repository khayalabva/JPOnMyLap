package day03_EscapeSequences; // package name of the class



public class Println_VS_Print2 {


    public static void main(String[] args) {   //main method

        System.out.println("Knock Knock");     // first it prints knock knock, then it appends a new line
        System.out.println("Who is this?");

        System.out.println("This is Java");
        System.out.println("-----------------------");

        System.out.print("Knock Knock");   // prints knock knock, does not appends a new line
        System.out.print("Who is this?");
        System.out.println("This is Java");

        System.out.println("----------------");


        System.out.println("Hello everyone! How are you today? today we will learn escape sequences!");

        System.out.println("-----------");

        System.out.print("Hello everyone! ");
        System.out.print("how are you today? ");
        System.out.println("today we will learn escape sequences!");


    }
}

